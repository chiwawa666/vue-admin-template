function generateRandomItems(count, lastIndex) {
  let items = []
  let i
  count = lastIndex
  // const date = new Date();
  for (i = 0; i < 100; i++) {
    items[i] = {
      index: array[i],
      time: time[(i % 8)],
      position: position[i],
      style: {
        background: getRandomColor(array[i])
      },
      width: 50,
      height: 50
    }
  }
  return items
}

function getRandomColor(str) {
  let colors = [
    'rgba(21,174,103,.5)',
    'rgba(245,163,59,.5)'
  ]
  if (str == '闲置') {
    return colors[1]
  } else if (str == '已预约') {
    return 'rgba(109, 96,80,0.5)'
  } else if (str == '废弃') {
    return 'rgba(58,119,183,0.5)'
  }
  else {
    return colors[0]
  }

  // return colors[~~(Math.random() * colors.length)]
}
